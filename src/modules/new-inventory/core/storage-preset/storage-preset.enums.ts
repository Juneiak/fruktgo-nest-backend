/**
 * Пресеты коэффициентов деградации для разных категорий товаров
 * Определяют как быстро товар теряет свежесть в зависимости от условий хранения
 */
export enum StoragePreset {
  /** Ягоды — высокая чувствительность к температуре */
  BERRIES = 'BERRIES',
  /** Косточковые фрукты (персики, сливы, вишня) */
  STONE_FRUITS = 'STONE_FRUITS',
  /** Цитрусовые — относительно устойчивы */
  CITRUS = 'CITRUS',
  /** Яблоки, груши — долгое хранение */
  APPLES_PEARS = 'APPLES_PEARS',
  /** Тропические фрукты (бананы, манго) — чувствительны к холоду */
  TROPICAL = 'TROPICAL',
  /** Листовая зелень — очень чувствительна */
  LEAFY_GREENS = 'LEAFY_GREENS',
  /** Корнеплоды — долгое хранение */
  ROOT_VEGETABLES = 'ROOT_VEGETABLES',
  /** Томаты — особый режим */
  TOMATOES = 'TOMATOES',
  /** Огурцы */
  CUCUMBERS = 'CUCUMBERS',
  /** Грибы — высокая чувствительность */
  MUSHROOMS = 'MUSHROOMS',
  /** Молочные продукты */
  DAIRY = 'DAIRY',
  /** Мясо */
  MEAT = 'MEAT',
  /** Рыба/морепродукты */
  SEAFOOD = 'SEAFOOD',
  /** Выпечка */
  BAKERY = 'BAKERY',
  /** Замороженные продукты */
  FROZEN = 'FROZEN',
  /** Консервы и бакалея — долгое хранение, низкая чувствительность */
  SHELF_STABLE = 'SHELF_STABLE',
  /** Общий пресет (по умолчанию) */
  GENERIC = 'GENERIC',
}

/**
 * Диапазоны температуры хранения
 * Коэффициент < 1.0 = товар портится медленнее
 * Коэффициент > 1.0 = товар портится быстрее
 */
export enum TemperatureRange {
  /** Морозильник (-18°C и ниже) */
  FREEZER = 'FREEZER',
  /** Холодильник (0-4°C) */
  COLD = 'COLD',
  /** Прохладное (10-15°C) */
  COOL = 'COOL',
  /** Комнатная (18-22°C) */
  ROOM = 'ROOM',
  /** Тёплое (>25°C) */
  WARM = 'WARM',
}

/**
 * Диапазоны влажности хранения
 */
export enum HumidityRange {
  /** Сухо (<50%) */
  DRY = 'DRY',
  /** Нормально (50-70%) */
  NORMAL = 'NORMAL',
  /** Влажно (70-90%) */
  HUMID = 'HUMID',
  /** Очень влажно (>90%) */
  VERY_HUMID = 'VERY_HUMID',
}

/**
 * Чувствительность товара к условиям хранения
 */
export enum StorageSensitivity {
  /** Низкая — консервы, крупы */
  LOW = 'LOW',
  /** Средняя — большинство овощей и фруктов */
  MEDIUM = 'MEDIUM',
  /** Высокая — ягоды, зелень, молочка */
  HIGH = 'HIGH',
}
